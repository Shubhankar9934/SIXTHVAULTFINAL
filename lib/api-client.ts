  /**
   * Enhanced query method with conversation support
   */
  async queryDocumentsWithConversation(
    question: string, 
    options: {
      hybrid?: boolean
      maxContext?: boolean
      documentIds?: string[]
      provider?: string
      model?: string
      conversationId?: string
      saveConversation?: boolean
    } = {}
  ): Promise<QueryResponse & { conversation_id?: string }> {
    const { 
      hybrid = this.hybrid, 
      maxContext = false, 
      documentIds, 
      provider = 'gemini', 
      model,
      conversationId,
      saveConversation = true
    } = options
    
    const url = new URL(`${this.baseUrl}/query`)
    url.searchParams.set('hybrid', hybrid.toString())
    url.searchParams.set('max_context', maxContext.toString())

    try {
      const response = await fetch(url.toString(), {
        method: 'POST',
        headers: {
          ...this.getAuthHeaders(),
          'Content-Type': 'application/json'
        },
        body: JSON.stringify({ 
          question,
          document_ids: documentIds,
          provider: provider,
          model: model,
          conversation_id: conversationId,
          save_conversation: saveConversation
        })
      })

      if (!response.ok) {
        const errorData = await response.json().catch(() => ({}))
        throw new Error(errorData.detail || `Query failed: ${response.status}`)
      }

      return await response.json()
    } catch (error) {
      console.error('Query error:', error)
      throw error
    }
  }

